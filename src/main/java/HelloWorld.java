public class HelloWorld {
    public String sayHello(){
        return "Hello World!";
    }
    public int Switch(int i){
        int b =0 ;
        switch(i){
            case 1:
                b=1;
                break;
            case 2:
                b=2;
                break;
                default:
                    b=3;
                    break;

        }
        return b;
    }

}
